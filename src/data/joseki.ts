export const WHITE_JOSEKI_LIST = JOSEKI_LIST.filter(
  (joseki) => joseki.color === "white",
);

export const BLACK_JOSEKI_LIST = JOSEKI_LIST.filter(
  (joseki) => joseki.color === "black",
);

export function findJosekiById(id: string): Joseki | undefined {
  return JOSEKI_LIST.find((joseki) => joseki.id === id);
}

export function findJosekiByMoves(moves: string[]): Joseki[] {
  const normalized = moves.map((move) => move.toLowerCase());
  return JOSEKI_LIST.filter((joseki) =>
    normalized.every(
      (move, index) => joseki.moves[index]?.toLowerCase() === move,
    ),
  );
}
